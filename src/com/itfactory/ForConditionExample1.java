package com.itfactory;

public class ForConditionExample1 {
    /**
     * Sa se afiseze numerele de la 0 la 20 pe aceeasi linie utilizand o bucla for
     *
     * @param args
     */
    public static void main(String[] args) {
        for (int i = 0; i <= 20; i++) {
            System.out.print(" " + i + " ");
        }

    }
}
